    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        String ten = this.txtTen.getText();
        String moTa = this.txtMoTa.getText();
        int trangThai = this.rdoInactive.isSelected() == true ? 0 : 1;
        
        DanhMuc danhMuc = new DanhMuc(0, ten, moTa, trangThai);
        boolean result = this.service.insert(danhMuc);
        
        if (result == true) {
            JOptionPane.showMessageDialog(this, "Thành công");
        } else {
            JOptionPane.showMessageDialog(this, "Thất bại");
        }
    }//GEN-LAST:event_btnThemActionPerformed

